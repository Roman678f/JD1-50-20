package operators;

public class OperatorsMain5 {

    public static void main(String[] args){
        int dayNumber = 3;

        switch(dayNumber){
            default:
                System.out.println("Неизвестно");
                break;
            case 1:
                System.out.println("Поенедельник");
                break;
            case 2:
                System.out.println("Вторник");
                break;
        }
    }

}
